    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        if(validar() == true ){
            cargaP = new CargarProductoNuevo();
            Marca marca = miListaM.get(miListaM.indexOf(cmbx_marcas.getSelectedItem().toString()));
            Proveedor proveedor = miListaP.get(miListaP.indexOf(cmbx_proveedores.getSelectedItem().toString()));
            Moneda moneda = miListaMon.get(miListaMon.indexOf(cmbx_moneda.getSelectedItem().toString()));
            
            cargaP.CargarProductoNuevo(txt_nombre.getText(),txt_codigo.getText(),Float.valueOf(txt_costo.getText()),
                    Float.valueOf(txt_precio.getText()), marca,
                    proveedor,txtA_descripcion.getText(),moneda);
            mainFrame.verTodosLosProductos();
            this.setVisible(false);
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

