    // Metodo que es llamado automaticamente por la maquina virtual Java cada vez que repinta
    public void paintComponent(Graphics g) {

        /* Obtenemos el tamaño del panel para hacer que se ajuste a este
         cada vez que redimensionemos la ventana y se lo pasamos al drawImage */
        int width = this.getSize().width;
        int height = this.getSize().height;

        // Mandamos que pinte la imagen en el panel
        if (this.background != null) {
            g.drawImage(this.background, 0, 0, width, height, null);
        }

        super.paintComponent(g);
    }
    
    private void traerMarcas(){
        traerM = new TraerMarcas();
        miListaM = new ArrayList<>();
        miListaM = traerM.TraerMarcas();
        for(int i = 0; i < miListaM.size(); i++){
            cmbx_marcas.addItem(miListaM.get(i).getNombre());
        }
    }
    
    private void traerMonedas(){
        traerMon = new TraerMonedas();
        miListaMon = new ArrayList<>();
        miListaMon = traerMon.traerMonedas();
        for (int i = 0; i < miListaMon.size() ; i++){
            cmbx_moneda.addItem(miListaMon.get(i));
        }
    }
    
    private void traerProveedores(){
        traerP = new TraerProveedores();
        miListaP = new ArrayList<>();
        miListaP = traerP.TraerProveedores();
        for(int i = 0; i < miListaP.size(); i++){
            cmbx_proveedores.addItem(miListaP.get(i).getNombre());
        }
    }

    // Metodo donde le pasaremos la dirección de la imagen a cargar.
    public void setBackground(String imagePath) {

        // Construimos la imagen y se la asignamos al atributo background.
        this.setOpaque(false);
        this.background = new ImageIcon(imagePath).getImage();
        repaint();
    }
    
    private boolean validar(){
        if(txt_nombre.getText().trim().length() > 0){
            if(txt_codigo.getText().trim().length() > 0){
                if(txt_precio.getText().trim().length() > 0){
                    return true;
                }else{
                    JOptionPane.showMessageDialog(this, "Ingese un precio!");
                    txt_precio.requestFocus();
                }
            }else{
                JOptionPane.showMessageDialog(this, "Ingese un codigo!");
                txt_codigo.requestFocus();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Ingese un nombre!");
            txt_nombre.requestFocus();
         }
        return false;
        
    }

